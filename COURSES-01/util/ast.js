function showAST(info) {
	console.log('======== AST ========');
	console.log('[fieldName]', info.fieldName);
	console.log('[returnType]', info.returnType);
	console.log('[parentType]', info.parentType);
	console.log('[cacheControl]', info.cacheControl);
	console.log('=====================');
	console.log('[info]', info);
	console.log('======== END ========');
}
